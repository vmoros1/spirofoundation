							<p align="center" style="text-align: center; margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma"><b>Growing <i>Aloe arborescens</i></b></font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma">The krantz aloe is an easy and 
							rewarding plant to grow, and is a popular garden 
							plant in many countries. It enjoys full sun, 
							well-drained, compost-enriched soil and can tolerate 
							moderate frost but is sensitive to severe frost. It 
							is fast-growing, and it will tolerate drought and 
							neglect once established. It is grown mainly as an 
							ornamental or as an accent plant, but is also an 
							excellent and impenetrable hedge plant.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma">The krantz aloe is easily 
							propagated from a branch or stem cut off, allowed to 
							dry for a day or so until the wound has sealed, and 
							then planted in well-drained soil or sand. They need 
							not be rooted in any particular place and then 
							transplanted, but can be placed directly into their 
							permanent place in the garden. It is important to 
							remember not to water the cuttings too heavily; 
							overwatering may cause them to rot. This aloe can 
							also be grown from seed, sown in spring. Seed should 
							take three to four weeks to germinate, and the 
							seedlings must be protected from frost. </font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma"><i>Aloe arborescens</i> 
							hybridises readily with other aloes.</font></p>
