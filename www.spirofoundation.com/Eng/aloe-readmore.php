							large colourful flower spikes are borne in profusion 
							during the cold winter months (May-July), 
							brightening up a drab winter garden. Deep orange is 
							the most common colour, but there are also pure 
							yellow forms, and an unusual bi-coloured form of 
							deep orange (almost red) and yellow. The 
							inflorescence is usually unbranched, with two to 
							several arising from a single rosette. As with all 
							the aloes, the flowers produce nectar and are 
							attractive to many kinds of birds, in particular the 
							small and colourful sunbirds, which flit from flower 
							to flower in search of nectar. The flowers also 
							attract bees.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma">The species formerly known as <i>
							<a style="color: #6666FF; text-decoration: underline; text-underline: single" href="http://www.plantzafrica.com/plantab/aloemutab.htm">
							Aloe mutabilis</a></i> is now regarded as a synonym 
							of <i>Aloe arborescens</i>. It is a cliff dwelling 
							form with smaller, less branched rosettes and red &amp; 
							yellow bi-coloured flower spikes and is more evident 
							on the high inland plateau of the northern provinces 
							of South Africa. This cliff dwelling form of <i>Aloe 
							arborescens</i> can be seen hanging from the cliffs 
							alongside the waterfall at the Witwatersrand 
							National Botanical Garden.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma">Although it is in fact a large 
							much-branched shrub,<i> Aloe arborescens</i> has 
							been allocated a national tree number (28.1).</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma"><b>Distribution</b><br>
							This species is distributed mainly over the eastern, 
							summer rainfall areas of the country. It has the 
							third widest distribution of any aloe, occurring 
							from the Cape Peninsula along the eastern coast, 
							through KwaZulu-Natal, Mpumalanga and Limpopo 
							province and further north into Mozambique, Zimbabwe 
							and Malawi. It is one of the few aloes that can be 
							found growing at sea level right up to the tops of 
							mountains. The krantz aloe is adapted to many 
							habitats, but is usually found in mountainous areas 
							where it favours exposed ridges and rocky outcrops. 
							It is also found in dense bush.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma"><b>Derivation of the name &amp; 
							historical aspects</b><br>
							The name aloe is from the Greek <i>alsos</i> and 
							refers to the bitter juice from the leaves of these 
							plants. It is probably derived from the earlier 
							Arabic word <i>alloeh </i>or the Hebrew word <i>
							allal</i>, both meaning bitter. The Latin word <i>
							arborescens</i> means tree-forming or tree-like, and 
							is a bit misleading in that this aloe is not really 
							tree-like, but the name was originally applied to 
							this species in reference to the stem-forming habit. 
							The common name krantz aloe refers to its habitat, a 
							krantz being a rocky ridge or cliff.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma"><i>Aloe arborescens</i> is one 
							of approximately 130 <i>Aloe</i> species native to 
							southern Africa. It is possibly the most widely 
							cultivated aloe in the world and can be seen grown 
							in gardens in many cities around the world. It was 
							one of the first South African aloes collected and 
							planted in the Company's Garden in Cape Town. It was 
							grown in Amsterdam by Professor Commelin in 1674, 
							and featured in <i>Hort. Amst. 2</i> in 1701.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma"><b>Uses and cultural aspects</b><br>
							In many parts of South Africa <i>Aloe arborescens</i> 
							is planted around kraals (domestic stock enclosures) 
							as a living fence. It often happens that the 
							position of old kraals can still be seen many years 
							after they have been abandoned because the aloes 
							persist. Cuttings intended for use as barrier plants 
							are sold in muthi shops.</font></p>
							<p align="center" style="text-align: center; margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma">&nbsp;</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
							<font face="Tahoma">The Zulu people use the leaves 
							of this plant, dried and pounded into a powder, as a 
							protection against storms. Decoctions of the leaves 
							are also used in childbirth and in treating sick 
							calves. In the Transkei it is used for stomach ache 
							and given to chickens to prevent them from getting 
							sick. In the Orient, this aloe is grown in domestic 
							gardens as a convenient first-aid treatment for burn 
							wounds and abrasions. In fact it was only after it 
							was used to treat irradiation burn victims of 
							Hiroshima that its healing properties received 
							attention from the West. Extracts from the leaves 
							have been widely investigated since then and shown 
							significant wound healing, anti-bacterial, 
							anti-ulcer, anti-inflammatory, anti-carcinogenic, 
							hypoglycaemic and also alopoeic activity. The leaves 
							have also been found to have purgative properties 
							and the leaf sap is reported to relieve x-ray burns.</font></p>
