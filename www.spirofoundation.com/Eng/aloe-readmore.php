							The krantz aloe develops into a multiheaded shrub 2 
							-3m high with striking grey green leaves arranged in 
							attractive rosettes. The leaf margins are armed with 
							conspicuous pale teeth.</font></p>
							<p style="margin-left: 7px; margin-right: 7px; margin-top: 12px; margin-bottom: 0">
