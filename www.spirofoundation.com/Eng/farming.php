					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0" align="center">&nbsp;</p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0" align="center">
					<b><font face="Tahoma" size="4">Organic farming and 
					bio-diversity</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0" align="center">&nbsp;</p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">
					<span lang="AR-EG" style="line-height: 150%">
					<img border="1" src="../images/mazare1.jpg" align="right"></span>&nbsp;The 
					Spiros Moros foundation firmly believes in returning to the 
					bosom of nature to ensure better life for man and all other 
					creatures.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">That encompasses going back to 
					organic farming, which depends on natural and environmental 
					equilibrium, as well as going back to nature which is a 
					veritable pharmacy full of natural remedies that protect 
					against and cure from many ailments and afflictions and 
					improve general health.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">&nbsp;</p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<b><font face="Tahoma" size="2">Do you need to know more 
					about organic farming?</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">What do we eat? That is a daily 
					recurring question and a daily nagging concern.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">We've come to yearn for the 
					long missing distinct aroma and natural taste of fruits and 
					vegetables. But more importantly we are now looking for food 
					which is safe and not saturated with pesticides and chemical 
					fertilizers and that is exactly what organic farming is all 
					about! </font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">What are the impacts &amp; organic 
					farming?</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Organic farming not only 
					positively impacts human-health, but also the health of 
					animals and fish alike.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Nor will our precious and long 
					suffering lakes, seas and rivers be further polluted by 
					chemically contaminated agricultural wastes and thus we'll 
					protect the fish and meat that we eat and the dairy that we 
					consume.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Organic farming protects both 
					the environment and the land being farmed, since it 
					preserves fertility of the soil and protects it from 
					pollution , thus allowing its renewal and restoration of the 
					natural agricultural cycles.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Organic farming also allows the 
					farming friendly birds as well as the various creatures 
					which interact with the agricultural process, to perform its 
					natural roles.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">&nbsp;</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">&nbsp;</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<b><font face="Tahoma" color="#FF0000" size="3">Int'l &nbsp;codes 
					for organic farming</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<b><font face="Tahoma" size="2">How can we determine whether 
					a given product is , indeed, organic or not?</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In the sixties 
					of the last century, there were ever-increasing calls for 
					the return to nature after the destructive impact, on man 
					and environment, of the extensive use of pesticides and 
					chemical fertilizers. In response to these mounting calls as 
					well as to several important research papers published by 
					Dr. Adolf Steiner and the distinguished efforts exerted by 
					the British Earth Society, a special accreditation system 
					for organic farming was founded and the US., not be outdone, 
					started passing legislations for that selfsame purpose.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">&nbsp;1972 witnessed the 
					establishment of the International Foundation For Organic 
					Agricultural Movements(IFOAM) ,which is an international 
					non-governmental body with around 750 affiliated 
					organizations currently spread over more than 100 countries.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">(IFOAM) works incessantly 
					towards establishing the basic criteria for organic farming 
					and organic products.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">1980 was a decisive turning 
					point on the long and difficult road toward spreading the 
					concept of organic farming since it witnessed the 
					proclamation of the basic relevant specifications of the (IFOAM).</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Almost immediately afterwards 
					the Food and Agriculture Organization (FAO) announced a food 
					code, defining the organic product. Also a European 
					Accreditation System was ratified. </font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">According to&nbsp; IFOAM's 
					specifications issued in 2001, organic farming is an 
					integrated approach based on a package of processes, which 
					will result in an integrated and sustainable biological 
					eco-system, safe food, proper nutrition, respect for the 
					rights and well-being of animals and social justice.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Thus organic farming, in its 
					broader sense, cares for each individual plant and helps it 
					to grow naturally, based on environmental and natural 
					components.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Organic farming doesn’t leave 
					animals out, since organic production of cattle emphasizes 
					the necessity for a health-driven management programme that 
					addresses the various environment factors to minimize 
					exhaustion and prevent diseases.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Therefore , most criteria for 
					organic production of cattle obligate cattle growers to 
					provide easy access for the animals to adequate space , 
					fresh air, open grazing, sunlight and shade as well as 
					protection of&nbsp; animals against inclement weather and 
					climatic swings. </font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">The same, above-mentioned, 
					criteria require a balanced feeding system, using organic 
					feeds and feed supplements which are free of hormones and 
					artificial growth enhancers.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Those criteria stipulate that 
					newly born calves and sheep be&nbsp; provided access to colostrum 
					before weaning and forbid that they be given alternative 
					formulas that contain chemical compounds.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Most organic production 
					certification boards ban the use of antibiotics, while some 
					of them recommend a specific abstinence period (medicinal 
					withdrawal period) which is usually twice or 3 times longer 
					than that recommended by the company manufacturing the drug 
					in the accompanying insert.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Contravening those criteria 
					disqualifies the animal and its products as organic.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">The use of vaccines, however, 
					is generally allowed with some restrictions, while the use 
					of growth enhancers and hormones is categorically banned.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">&nbsp;</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<b><font face="Tahoma" color="#FF0000" size="3">Challenges 
					facing organic farming:</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">&nbsp;</p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<b><font face="Tahoma" size="2">1- Low productivity per unit 
					area of farming land.</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Studies conducted by (FAO) 
					showed that the productivity of organic farming is less than 
					the corresponding productivity of chemical-based, 
					conventional farming and while this relatively low 
					productivity increases the soil concentrations of some of 
					the nutrients, thus making the product more pest-resistant, 
					yet the same studies suggest that with proper management, 
					the productivity of organic farming can be substantially 
					improved.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">From an economic perspective, 
					organic farmers will have to make up for low productivity 
					per unit area of farming land by increasing the price of 
					their products, which will not go well with many people all 
					over the world, especially in the so-called 3rd world, which 
					is made up of poor&quot; developing&quot; nations.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Therefore , it is of the utmost 
					importance that states take firm measures by passing 
					legislations , approving laws, establishing criteria and 
					above all by providing incentives to organic farmers either 
					directly or indirectly through subsidizing organic 
					fertilizers and pesticides, in addition to raising 
					environmental and nutritional awareness as well as awareness 
					of the value added by organic farming</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<b><font face="Tahoma" size="2">2- Alleged micro-biologic 
					contamination of organic products.</font></b></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">This allegation is refuted by 
					scientific studies which assert that good agricultural 
					practices, as well as good handling, storing and 
					manufacturing practices decrease the potential of fungal 
					growth.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">Furthermore , the application 
					of safety and quality control criteria will practically 
					nullify any such danger.</font></p>
					<p style="margin-left: 12px; margin-right: 12px; margin-top: 7px; margin-bottom: 0">
					<font face="Tahoma" size="2">&nbsp;Also , compost manufactured of 
					animal dung (manure) and used as organic fertilizer has come 
					under attack, but compost-if properly prepared- will 
					invariably yield excellent results.</font></p>
